package com.bizitglobal.tarjetafiel.fondos.exception;
/**
*	Es lanzada cuando existe en la base de datos una RegistroUpload con el mismo
*	identificador que la actividad que se quiere grabar.
*/
public class RegistroUploadDuplicateException extends RegistroUploadException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4391607614041750142L;

	/**
	* Constructor con argumentos para la RegistroUpload.
	* @param msg, Mensaje descriptivo de la excepcion.
	* @param cause, Causa de la excepcion.
	*/
	public RegistroUploadDuplicateException (String msg, Throwable cause) {

		super(msg,cause);
	}
}

