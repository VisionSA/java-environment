package com.bizitglobal.tarjetafiel.fondos.exception;
/**
*	Padre de las excepciones que ocurren en la clase.
*/
public class ConciliacionFondoException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 4000143854994260082L;
	/**
	* Constructor por defecto de la excepcion.
	* @param msg, Mensaje descriptivo de la excepcion.
	*/
	public ConciliacionFondoException (String msg) {

		super(msg);
	}
	/**
	* Constructor con argumentos para la excepcion.
	* @param msg, Mensaje descriptivo de la excepcion.
	* @param cause, Causa raiz de la excepcion.
	*/
	public ConciliacionFondoException (String msg, Throwable cause) {

		super(msg,cause);
	}
}

