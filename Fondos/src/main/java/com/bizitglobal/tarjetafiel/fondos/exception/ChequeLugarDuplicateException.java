package com.bizitglobal.tarjetafiel.fondos.exception;
/**
*	Es lanzada cuando existe en la base de datos una ChequeLugar con el mismo
*	identificador que la actividad que se quiere grabar.
*/
public class ChequeLugarDuplicateException extends ChequeLugarException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8746200865235641613L;

	/**
	* Constructor con argumentos para la ChequeLugar.
	* @param msg, Mensaje descriptivo de la excepcion.
	* @param cause, Causa de la excepcion.
	*/
	public ChequeLugarDuplicateException (String msg, Throwable cause) {

		super(msg,cause);
	}
}

