package com.bizitglobal.tarjetafiel.fondos.exception;
/**
*	Es lanzada cuando existe en la base de datos una CajaMP con el mismo
*	identificador que la actividad que se quiere grabar.
*/
public class CajaMPDuplicateException extends CajaMPException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1243513924294427541L;

	/**
	* Constructor con argumentos para la CajaMP.
	* @param msg, Mensaje descriptivo de la excepcion.
	* @param cause, Causa de la excepcion.
	*/
	public CajaMPDuplicateException (String msg, Throwable cause) {

		super(msg,cause);
	}
}

