package com.bizitglobal.tarjetafiel.fondos.exception;
/**
*	Es lanzada cuando existe en la base de datos una Asiento con el mismo
*	identificador que la actividad que se quiere grabar.
*/
public class AsientoFondosDuplicateException extends AsientoFondosException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7712597220748933033L;

	/**
	* Constructor con argumentos para la Asiento.
	* @param msg, Mensaje descriptivo de la excepcion.
	* @param cause, Causa de la excepcion.
	*/
	public AsientoFondosDuplicateException (String msg, Throwable cause) {

		super(msg,cause);
	}
}

