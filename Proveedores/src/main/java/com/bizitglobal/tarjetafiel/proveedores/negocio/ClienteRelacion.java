package com.bizitglobal.tarjetafiel.proveedores.negocio;

public class ClienteRelacion {
	private Long idClienteProveedor;
	private Long idCliente;
	private String observacion;
	private Proveedor proveedor;
	
	public ClienteRelacion() {
		this(null,null,null,null);
	}
	
	public ClienteRelacion(Long idClienteProveedor, Long idCliente, String observacion, Proveedor proveedor) {
		super();
		this.idClienteProveedor = idClienteProveedor;
		this.idCliente = idCliente;
		this.observacion = observacion;
		this.proveedor = proveedor;
	}

	public Long getIdCliente() {
		return idCliente;
	}
	
	public void setIdCliente(Long idCliente) {
		this.idCliente = idCliente;
	}
	
	public Long getIdClienteProveedor() {
		return idClienteProveedor;
	}
	
	public void setIdClienteProveedor(Long idClienteProveedor) {
		this.idClienteProveedor = idClienteProveedor;
	}
	
	public String getObservacion() {
		return observacion;
	}
	
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}
	
	public Proveedor getProveedor() {
		return proveedor;
	}
	
	public void setProveedor(Proveedor proveedor) {
		this.proveedor = proveedor;
	}
}
