package com.bizitglobal.tarjetafiel.proveedores.exception;

/**
 * Es lanzada cuando existe en la base de datos un DocumentoAdjunto con el mismo
 * identificador que el DocumentoAdjunto que se quiere grabar.
 * @author Waldemar
 */
public class DocumentoAdjuntoDuplicateException extends DocumentoAdjuntoException {



	/**
	 * 
	 */
	private static final long serialVersionUID = -1879750653568758219L;

	/**
	 * Constructor con argumentos para la excepción.
	 * @param msg, Mensaje descriptivo de la excepción.
	 * @param cause, Causa de la excepción.
	 */
	public DocumentoAdjuntoDuplicateException(String msg, Throwable cause) {
		super(msg,cause);
	}
	
}
