package com.bizitglobal.tarjetafiel.proveedores.negocio;

import com.bizitglobal.tarjetafiel.general.negocio.Domicilio;

public class ProveedorDomicilio {
	private Long idProvDomicilio;
	private Domicilio domicilio;
	private Proveedor proveedor;
	
	public ProveedorDomicilio() {
		this(null,null);
	}
	
	public ProveedorDomicilio(Domicilio domicilio, Proveedor proveedor) {
		super();
		idProvDomicilio = null;
		this.domicilio = domicilio;
		this.proveedor = proveedor;
	}

	public Domicilio getDomicilio() {
		return domicilio;
	}
	
	public void setDomicilio(Domicilio domicilio) {
		this.domicilio = domicilio;
	}
	
	public Long getIdProvDomicilio() {
		return idProvDomicilio;
	}
	
	public void setIdProvDomicilio(Long idProvDomicilio) {
		this.idProvDomicilio = idProvDomicilio;
	}
	
	public Proveedor getProveedor() {
		return proveedor;
	}
	
	public void setProveedor(Proveedor proveedor) {
		this.proveedor = proveedor;
	}

}
