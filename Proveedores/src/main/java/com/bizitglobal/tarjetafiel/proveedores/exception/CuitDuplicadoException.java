package com.bizitglobal.tarjetafiel.proveedores.exception;

/**
 * Es lanzada cuando se intenta ingresar un cuit duplicado.
 * @author Daniel
 */
public class CuitDuplicadoException extends Exception {
	
	/**
	 * Constructor por defecto de la excepción.
	 * @param msg, Mensaje descriptivo de la excepción.
	 */
	public CuitDuplicadoException(String msg) {
		super(msg);
	}
	
	/**
	 * Constructor con argumentos para la excepción.
	 * @param msg, Mensaje descriptivo de la excepción.
	 * @param cause, Causa raiz de la excepción.
	 */
	public CuitDuplicadoException(String msg,Throwable cause) {
		super(msg,cause);
	}	

}
