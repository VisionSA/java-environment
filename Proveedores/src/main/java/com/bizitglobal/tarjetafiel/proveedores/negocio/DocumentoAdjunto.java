package com.bizitglobal.tarjetafiel.proveedores.negocio;

import org.apache.log4j.Logger;


public class DocumentoAdjunto {
	private static final Logger log = Logger.getLogger(DocumentoAdjunto.class);

	private Long idDocumentoAdjunto;
	private Long id;
	private Comprobante comprobante;
    private String url;

    public DocumentoAdjunto() {
    	
    }
    
    public DocumentoAdjunto(String url) {
    	this.url = url;
    }
    
    public DocumentoAdjunto(String url, Comprobante comprobante) {
    	this.url = url;
    	this.comprobante = comprobante;
    }
    
	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public Comprobante getComprobante() {
		return comprobante;
	}

	public void setComprobante(Comprobante comprobante) {
		this.comprobante = comprobante;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getIdDocumentoAdjunto() {
		return idDocumentoAdjunto;
	}

	public void setIdDocumentoAdjunto(Long idDocumentoAdjunto) {
		this.idDocumentoAdjunto = idDocumentoAdjunto;
	}


	
}
