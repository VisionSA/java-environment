package com.bizitglobal.tarjetafiel.proveedores.exception;

/**
 * Padre de las excepciones que ocurren en DocumentoAdjunto.
 * @author Daniel
 */
public class DocumentoAdjuntoException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 3187534747975731816L;

	/**
	 * Constructor por defecto de la excepción.
	 * @param msg, Mensaje descriptivo de la excepción.
	 */
	public DocumentoAdjuntoException(String msg) {
		super(msg);
	}
	
	/**
	 * Constructor con argumentos para la excepción.
	 * @param msg, Mensaje descriptivo de la excepción.
	 * @param cause, Causa raiz de la excepción.
	 */
	public DocumentoAdjuntoException(String msg,Throwable cause) {
		super(msg,cause);
	}	

}
