package com.bizitglobal.tarjetafiel.planificadoremail.negocio;

public class Contacts {
	
	String nombre;
	String vencimiento;
	String total;
	String estado_cuenta;
	String headerDocumentUrl;
	String id_cliente;
	String idCliente;
	private String headerDocumentCaption;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getVencimiento() {
		return vencimiento;
	}
	public void setVencimiento(String vencimiento) {
		this.vencimiento = vencimiento;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getEstado_cuenta() {
		return estado_cuenta;
	}
	public void setEstado_cuenta(String estado_cuenta) {
		this.estado_cuenta = estado_cuenta;
	}
	public String getHeaderDocumentUrl() {
		return headerDocumentUrl;
	}
	public void setHeaderDocumentUrl(String headerDocumentUrl) {
		this.headerDocumentUrl = headerDocumentUrl;
	}
	public String getId_cliente() {
		return id_cliente;
	}
	public void setId_cliente(String id_cliente) {
		this.id_cliente = id_cliente;
	}
	public String getIdCliente() {
		return idCliente;
	}
	public void setIdCliente(String idCliente) {
		this.idCliente = idCliente;
	}
	public String getHeaderDocumentCaption() {
		return headerDocumentCaption;
	}
	public void setHeaderDocumentCaption(String headerDocumentCaption) {
		this.headerDocumentCaption = headerDocumentCaption;
	}
	
	

}
