package com.bizitglobal.tarjetafiel.planificadoremail.negocio;

import java.util.Set;


/***** @Id:6958 ******/
public class Template {

	private Long idTemplate;
	private String descripcion;
	private String archivoTemp;
	private Set urlTemplate;
	private Set paramTemplate;
	private TipoOrigen tipoOrigen;


	public Template() {

	}


	public Long getIdTemplate() {
		return idTemplate;
	}


	public void setIdTemplate(Long idTemplate) {
		this.idTemplate = idTemplate;
	}


	public String getDescripcion() {
		return descripcion;
	}


	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}


	public String getArchivoTemp() {
		return archivoTemp;
	}


	public void setArchivoTemp(String archivoTemp) {
		this.archivoTemp = archivoTemp;
	}


	public Set getUrlTemplate() {
		return urlTemplate;
	}


	public void setUrlTemplate(Set urlTemplate) {
		this.urlTemplate = urlTemplate;
	}


	public TipoOrigen getTipoOrigen() {
		return tipoOrigen;
	}


	public void setTipoOrigen(TipoOrigen tipoOrigen) {
		this.tipoOrigen = tipoOrigen;
	}


	public Set getParamTemplate() {
		return paramTemplate;
	}


	public void setParamTemplate(Set paramTemplate) {
		this.paramTemplate = paramTemplate;
	}

}
