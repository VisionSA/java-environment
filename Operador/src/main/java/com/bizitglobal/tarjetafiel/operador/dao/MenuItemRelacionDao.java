package com.bizitglobal.tarjetafiel.operador.dao;

import java.util.List;

import com.bizitglobal.tarjetafiel.operador.negocio.MenuItemRelacion;;

/**
 * Interface que se encarga de definir las operaciones que se podran efectuar con la base de datos.
 */
public interface MenuItemRelacionDao {
	
	/**
	 * Graba un MenuItemRelacion en la base de datos.
	 * @param unMenuItemRelacion, MenuItemRelacion a grabar en la base de datos.
	 */
	public void saveMenuItemRelacion(MenuItemRelacion unMenuItemRelacion);
	
	/**
	 * Obtiene un MenuItemRelacion de la base de datos, dado un id de MenuItemRelacion.
	 * @param id, Id del MenuItemRelacion buscado.
	 * @return Retorna el MenuItemRelacion buscado.
	 */
	public MenuItemRelacion findMenuItemRelacion(Long id);
	
	/**
	 * Borra un MenuItemRelacion de la base de datos, dado un id de MenuItemRelacion.
	 * @param id, Id del MenuItemRelacion a borrar.
	 */
	public void deleteMenuItemRelacion(Long id);
	
	/**
	 * Borra un MenuItemRelacion de la base de datos, dado el mismo MenuItemRelacion.
	 * @param unMenuItemRelacion, MenuItemRelacion a borrar.
	 */
	public void deleteMenuItemRelacion(MenuItemRelacion unMenuItemRelacion);
	
	
	/**
	 * Actualiza un MenuItemRelacion existente en el base de datos.
	 * @param unMenuItemRelacion, MenuItemRelacion a actualizar.
	 */
	public void updateMenuItemRelacion(MenuItemRelacion unMenuItemRelacion);
	
	/**
	 * Obtiene una lista con todos los MenuItemRelacions del sistema.
	 * @return Retorna una lista con todos los MenuItemRelacions.
	 */
	public List listAll();
	
}
