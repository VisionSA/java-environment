package com.print;

import javax.print.attribute.standard.OrientationRequested;

/**
 *
 * @author sporta
 */
public enum OrientacionHoja {
    
    VERTICAL(OrientationRequested.PORTRAIT),
    APAIZADA(OrientationRequested.LANDSCAPE);

    private final OrientationRequested orientacion;
    private OrientacionHoja(OrientationRequested orientacion) {
        this.orientacion = orientacion;
    }

    public OrientationRequested getOrientacion() {
        return orientacion;
    }

    


}
