package com.bizitglobal.webapp.faces.beans.general.wrapper;

import com.bizitglobal.tarjetafiel.general.negocio.SucEmail;


public class EmailWrapper {

	private int codigo;
	private SucEmail sucEmail;


	public EmailWrapper(int codigo, SucEmail sucEmail) {
		this.codigo = codigo;
		this.sucEmail = sucEmail;
	}


	public int getCodigo() {
		return codigo;
	}


	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}


	public SucEmail getSucEmail() {
		return sucEmail;
	}


	public void setSucEmail(SucEmail sucEmail) {
		this.sucEmail = sucEmail;
	}

}
