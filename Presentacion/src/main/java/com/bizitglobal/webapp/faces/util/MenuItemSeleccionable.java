package com.bizitglobal.webapp.faces.util;

import com.bizitglobal.tarjetafiel.operador.negocio.MenuItem;


public class MenuItemSeleccionable {
	private MenuItem menuItem;
	private boolean seleccionado;


	public MenuItemSeleccionable() {
		this(null, false);
	}


	public MenuItemSeleccionable(MenuItem menuItem, boolean seleccionado) {
		super();
		this.menuItem = menuItem;
		this.seleccionado = seleccionado;
	}


	public MenuItem getMenuItem() {
		return menuItem;
	}


	public void setMenuItem(MenuItem menuItem) {
		this.menuItem = menuItem;
	}


	public boolean getSeleccionado() {
		return seleccionado;
	}


	public void setSeleccionado(boolean seleccionado) {
		this.seleccionado = seleccionado;
	}

}
