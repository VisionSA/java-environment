package com.bizitglobal.tarjetafiel.impuestos.exception;

/**
 * Lanzada cuando el impuesto no existe en la base de datos.
 * @author Daniel
 */
public class ImpuestosIndividuoNotFoundException extends ImpuestosIndividuoException {


	/**
	 * 
	 */
	private static final long serialVersionUID = 2479954161483109481L;

	/**
	 * Constructor con argumentos para la excepción
	 * @param msg, Mensaje descriptivo de la excepción.
	 * @param cause, Causa de la excepción.
	 */
	public ImpuestosIndividuoNotFoundException(String msg, Throwable cause) {
		super(msg,cause);
	}
	
}
