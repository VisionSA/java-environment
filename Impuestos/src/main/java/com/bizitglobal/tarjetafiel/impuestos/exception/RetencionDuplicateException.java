package com.bizitglobal.tarjetafiel.impuestos.exception;

/**
 * Es lanzada cuando existe en la base de datos una retencion con el mismo
 * identificador que la retencion que se quiere grabar.
 * @author Daniel
 */
public class RetencionDuplicateException extends RetencionException {
	private static final long serialVersionUID = -5610948694723631993L;

	/**
	 * Constructor con argumentos para la excepción.
	 * @param msg, Mensaje descriptivo de la excepción.
	 * @param cause, Causa de la excepción.
	 */
	public RetencionDuplicateException(String msg, Throwable cause) {
		super(msg,cause);
	}
	
}
